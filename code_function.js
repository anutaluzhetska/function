console.log("functions:")

function simpleSquare(x) {
    return x * x
}

function mathSquare(x) {
    return Math.pow (x, 2)
}

function simpleCub(x) { 
    return x * x * x
}

function mathCub(x) {
    return Math.pow (x, 3)
}

console.log("First number square:", simpleSquare(8)) 
console.log("Second number square:", mathSquare(3)) 
console.log("First number cub:", simpleCub(8))
console.log("Second number cub:", mathCub(3))

 
